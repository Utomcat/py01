# 定义变量
a = 1
b = 2
c = 3

# 算术运算符
# +    ==>  3
print(a + b)

# -    ==> -1
print(a - b)

# *    ==> 2
print(a * b)

# /    ==> 0.5
print(a / b)

# %    ==> 1
print(a % b)

# **   ==> 1
print(a ** b)
# **   ==> 8
print(b ** c)

# //   ==> 0
print(a // b)
# //   ==> -1
print(a // -b)